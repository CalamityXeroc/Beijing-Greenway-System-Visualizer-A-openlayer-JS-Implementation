/* ──────────────────────────── 意图识别 ──────────────────────────── */

const ROUTE_PATTERNS = [
  /推荐|建议|哪条|哪个|适合|骑行|步行|散步|亲子|老人|儿童|家庭|朋友|情侣|难度|体力/,
  /哪里|哪儿|去哪|附近|周边|公里|路线|线路|出发|终点/
]

const TUTORIAL_PATTERNS = [
  /怎么|如何|操作|使用|功能|工具|测量|绘制|导出|图层|切换|按钮|帮助|教程|说明/,
  /测距|面积|画|标注|截图|设置|主题|深色|夜间/
]

/**
 * 根据用户输入自动选择最合适的 System Prompt
 * @param {string} message 用户输入
 * @returns {string} 对应的 prompt 字符串
 */
export function selectPrompt(message) {
  const text = message.toLowerCase()

  // 优先匹配教程类
  if (TUTORIAL_PATTERNS.some(re => re.test(text))) {
    return PROMPTS.PLATFORM_TUTORIAL
  }

  // 其次路线推荐类
  if (ROUTE_PATTERNS.some(re => re.test(text))) {
    return PROMPTS.ROUTE_RECOMMENDATION
  }

  // 默认
  return PROMPTS.GENERAL
}

